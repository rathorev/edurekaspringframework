package co.edureka;

public class Car {
	private String name;
	private int perDayCost;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPerDayCost() {
		return perDayCost;
	}
	public void setPerDayCost(int perDayCost) {
		this.perDayCost = perDayCost;
	}
	

}
